def draw_boxes(img_path, label_path):
    img = cv2.cvtColor(cv2.imread(str(img_path)), cv2.COLOR_BGR2RGB)
    H, W = img.shape[:2]
    if label_path.exists():
        for line in open(label_path).readlines():
            p = line.strip().split()
            if len(p) != 5: continue
            cls = int(p[0])
            xc,yc,w,h = float(p[1]),float(p[2]),float(p[3]),float(p[4])
            x1,y1 = int((xc-w/2)*W), int((yc-h/2)*H)
            x2,y2 = int((xc+w/2)*W), int((yc+h/2)*H)
            cv2.rectangle(img,(x1,y1),(x2,y2),COLORS[cls],1)
            cv2.putText(img,CLASS_NAMES[cls],(x1,max(y1-3,0)),
                        cv2.FONT_HERSHEY_SIMPLEX,0.35,COLORS[cls],1)
    return img

