"""## Cell 4 — Convert VisDrone Annotations to YOLO Format
VisDrone format: `[x, y, w, h, score, class, truncation, occlusion]`  
YOLO format: `[class_id, x_center_norm, y_center_norm, w_norm, h_norm]`
"""

CLASS_NAMES = ["pedestrian","people","bicycle","car","van",
               "truck","tricycle","awning-tricycle","bus","motor"]

def visdrone_to_yolo(split_dir: Path):
    ann_dir   = split_dir / "annotations"
    img_dir   = split_dir / "images"
    label_dir = split_dir / "labels"
    label_dir.mkdir(exist_ok=True)
    count = 0
    for ann_file in sorted(ann_dir.glob("*.txt")):
        img_path = img_dir / (ann_file.stem + ".jpg")
        if not img_path.exists():
            continue
        img_w, img_h = Image.open(img_path).size
        lines = []
        for row in open(ann_file).readlines():
            p = row.strip().split(",")
            if len(p) < 8:
                continue
            x, y, w, h, _, cls = int(p[0]),int(p[1]),int(p[2]),int(p[3]),p[4],int(p[5])
            if cls == 0 or cls > 10 or w <= 0 or h <= 0:
                continue
            xc = max(0., min(1., (x + w/2) / img_w))
            yc = max(0., min(1., (y + h/2) / img_h))
            wn = max(0., min(1., w / img_w))
            hn = max(0., min(1., h / img_h))
            lines.append(f"{cls-1} {xc:.6f} {yc:.6f} {wn:.6f} {hn:.6f}")
        open(label_dir / ann_file.name, "w").write("\n".join(lines))
        count += 1
    return count

for split in ["VisDrone2019-DET-train", "VisDrone2019-DET-val", "VisDrone2019-DET-test-dev"]:
    p = DATASET_DIR / split
    if p.exists():
        n = visdrone_to_yolo(p)
        print(f"{split}: {n} files converted")

"""## Cell 5 — Exploratory Data Analysis"""

def analyze(split_dir):
    stats = {"class_counts": defaultdict(int), "areas": [],
             "objs_per_img": [], "small":0, "medium":0, "large":0, "total":0}
    for lf in (split_dir / "labels").glob("*.txt"):
        n = 0
        for line in open(lf).readlines():
            p = line.strip().split()
            if len(p) != 5: continue
            cls, w, h = int(p[0]), float(p[3]), float(p[4])
            area = w * h
            stats["class_counts"][CLASS_NAMES[cls]] += 1
            stats["areas"].append(area)
            stats["total"] += 1
            n += 1
            if area < 0.001:   stats["small"]  += 1
            elif area < 0.01:  stats["medium"] += 1
            else:              stats["large"]  += 1
        stats["objs_per_img"].append(n)
    return stats

